
#También podemos convertir a intenger (int)

num1 = 12.3
print(num1, type(num1))
#[output] 12.3 <class 'float'>
#como vemos aqui nuestra variable tiene como valor agregado un decimal, que por efecto sera de tipo "float"
#Ahora convirtamoslo de "float" a "Interger"

num1 = int(12.3)
print(num1, type(num1))
#[output] 12 <class 'int'>
#Como podemos observar lo que hizo "intenger", fue que todo lo que estaba de lado derecho de la coma desapareciera
#quedando de esta forma un número entero, que por efecto sera de tipo "int"