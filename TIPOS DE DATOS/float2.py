
#También podemos usar float para declarar usando la notación científica con "e" y el exponente

#Aquí tenemos que 1.93 esta multiplicando a 10 elevado a la -3
f = 1.93 * 10 ** (-3)
print(f)

#Aquí decimos lo mismo solo que de forma mas resumida y sin tanto signo
f = 1.93e-3
print(f)

#[output] 0.0193